class MainActivity : ComponentActivity() {
    val customScope = CoroutineScope(SupervisorJob() + Dispatchers.Default)
    private val job = SupervisorJob()
    private var server: MainServer<TicTacToeGame.GameMove>? = null

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)

        setContent {
            appTheme {
                Surface(
                    modifier = Modifier.fillMaxSize(),
                    color = MaterialTheme.colorScheme.background,
                ) {
                    serverClientApp()
                }
            }
        }
    }

    override fun onDestroy() {
        super.onDestroy()
        job.cancel()
        server?.destroyConnection()
        server = null
    }

    @OptIn(ExperimentalMaterial3Api::class)
    @Composable
    fun serverClientApp() {
        var mode by remember { mutableStateOf("client") }
        var port by remember { mutableStateOf("8080") }
        var game by remember { mutableStateOf("TicTacToe") }
        var status by remember { mutableStateOf("–ù–µ –ø–æ–¥–∫–ª—é—á–µ–Ω–æ") }
        var isConnected by remember { mutableStateOf(false) }
        var ipInputVisible by remember { mutableStateOf(true) }
        var manualIp by remember { mutableStateOf("") }
        var serverName by remember { mutableStateOf("") }
        val currentGame =
            remember { TicTacToeComposable(this@MainActivity) { return@TicTacToeComposable status } }
        Box(modifier = Modifier.fillMaxSize()) {
            Column(
                modifier = Modifier.fillMaxSize().padding(16.dp),
                verticalArrangement = Arrangement.spacedBy(16.dp),
            ) {
                Text("–í—ã–±–µ—Ä–∏—Ç–µ —Ä–µ–∂–∏–º:", style = MaterialTheme.typography.titleMedium)

                Row(
                    verticalAlignment = Alignment.CenterVertically,
                    horizontalArrangement = Arrangement.spacedBy(8.dp),
                ) {
                    RadioButton(
                        selected = mode == "client",
                        onClick = {
                            mode = "client"
                            ipInputVisible = true
                        },
                    )
                    Text("–ö–ª–∏–µ–Ω—Ç")

                    Spacer(modifier = Modifier.width(16.dp))

                    RadioButton(
                        selected = mode == "server",
                        onClick = {
                            mode = "server"
                            ipInputVisible = false
                        },
                    )
                    Text("–°–µ—Ä–≤–µ—Ä")
                }

                OutlinedTextField(
                    value = port,
                    onValueChange = { port = if (it.toIntOrNull() != null) it else port },
                    label = { Text("–ü–æ—Ä—Ç") },
                    singleLine = true,
                    keyboardOptions = KeyboardOptions.Default.copy(keyboardType = KeyboardType.Number),
                    modifier = Modifier.fillMaxWidth(),
                )

                if (ipInputVisible) {
                    OutlinedTextField(
                        value = manualIp,
                        onValueChange = { manualIp = it },
                        label = { Text("IP —Å–µ—Ä–≤–µ—Ä–∞ (–æ—Å—Ç–∞–≤—å—Ç–µ –ø—É—Å—Ç—ã–º –¥–ª—è –∞–≤—Ç–æ–ø–æ–∏—Å–∫–∞ —Å–µ—Ä–≤–µ—Ä–æ–≤)") },
                        singleLine = true,
                        modifier = Modifier.fillMaxWidth(),
                    )
                } else {
                    OutlinedTextField(
                        value = serverName,
                        onValueChange = { serverName = it },
                        label = { Text("–ò–º—è —Å–µ—Ä–≤–µ—Ä–∞(–µ—Å–ª–∏ —Ö–æ—Ç–∏—Ç–µ –∏–º—è —Å–µ—Ä–≤–µ—Ä–∞, —Å–æ–≤–ø–∞–¥–∞—é—â–∏–º —Å IP, –æ—Å—Ç–∞–≤—å—Ç–µ –ø–æ–ª–µ –ø—É—Å—Ç—ã–º)") },
                        singleLine = true,
                        modifier = Modifier.fillMaxWidth(),
                    )
                }

                OutlinedTextField(
                    value = game,
                    onValueChange = { game = it },
                    label = { Text("–ù–∞–∑–≤–∞–Ω–∏–µ –∏–≥—Ä—ã: ") },
                    singleLine = true,
                    modifier = Modifier.fillMaxWidth(),
                )
                Button(
                    onClick = {
                        if (mode == "server") {
                            lifecycleScope.launch(Dispatchers.IO) {
                                customScope
                                    .launch {
                                        server =
                                            MainServer(
                                                currentGame,
                                                port.toInt(),
                                                onStatusUpdate = { newStatus ->
                                                    status = newStatus
                                                    println("--------SERVER--------\n$newStatus")
                                                },
                                                setGameResult = { newGameResult ->
                                                    currentGame.gameResult = newGameResult
                                                },
                                            )
                                        if (serverName != "") {
                                            server?.setNewServerName(serverName)
                                        }
                                        server?.startServer()
                                        isConnected = true
                                    }.join()
                            }
                        } else {
                            lifecycleScope.launch(Dispatchers.IO) {
                                customScope
                                    .launch {
                                        val client =
                                            ClientComposable(
                                                currentGame,
                                                port.toInt(),
                                                this@MainActivity,
                                                onStatusUpdate = { newStatus ->
                                                    status = newStatus
                                                    println("--------CLIENT--------\n$newStatus")
                                                },
                                                setGameResult = { newGameResult ->
                                                    currentGame.gameResult = newGameResult
                                                },
                                            )

                                        val selectedIp =
                                            if (manualIp.isNotBlank()) manualIp else client.selectGoodServer()
                                        if (selectedIp != null) {
                                            client.startClient(selectedIp)
                                            isConnected = true
                                        } else {
                                            status = "üî¥ –°–µ—Ä–≤–µ—Ä –Ω–µ –≤—ã–±—Ä–∞–Ω –∏–ª–∏ –Ω–µ –Ω–∞–π–¥–µ–Ω"
                                        }
                                    }.join()
                            }
                        }
                    },
                    modifier = Modifier.fillMaxWidth(),
                ) {
                    Text(if (mode == "server") "–ó–∞–ø—É—Å—Ç–∏—Ç—å —Å–µ—Ä–≤–µ—Ä" else "–ü–æ–¥–∫–ª—é—á–∏—Ç—å—Å—è")
                }

                Text(
                    text = status,
                    style = MaterialTheme.typography.bodyLarge,
                    color = if (isConnected) MaterialTheme.colorScheme.primary else MaterialTheme.colorScheme.error,
                )
            }
            IconButton(
                onClick = { finish() },
                modifier =
                    Modifier
                        .align(Alignment.TopEnd)
                        .padding(16.dp),
            ) {
                Icon(
                    imageVector = Icons.Default.Close,
                    contentDescription = "–ó–∞–∫—Ä—ã—Ç—å –ø—Ä–∏–ª–æ–∂–µ–Ω–∏–µ",
                    tint = MaterialTheme.colorScheme.onBackground,
                )
            }
        }
    }
}

@Composable
fun appTheme(content: @Composable () -> Unit) {
    MaterialTheme(
        colorScheme = lightColorScheme(),
        content = content,
    )
}
